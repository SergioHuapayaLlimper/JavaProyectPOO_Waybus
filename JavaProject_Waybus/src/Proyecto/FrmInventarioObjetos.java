    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = TableObjetosInfo.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.");
            return;
        }

        String codigo = TableObjetosInfo.getValueAt(filaSeleccionada, 0).toString(); // Columna 0 = Código

        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar el objeto con código " + codigo + "?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            MantenimientoLogistica.eliminarObjetoPorCodigo(codigo);
            ((DefaultTableModel) TableObjetosInfo.getModel()).removeRow(filaSeleccionada);
            JOptionPane.showMessageDialog(this, "Objeto eliminado correctamente.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

