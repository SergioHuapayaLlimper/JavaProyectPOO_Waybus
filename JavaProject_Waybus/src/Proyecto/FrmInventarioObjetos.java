    private void btnIniciarCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarCodigoActionPerformed
        // Obtener el valor de búsqueda de Código
        String codigo = txtBusquedaCodigo.getText().toLowerCase();

        // Obtener el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) TableObjetosInfo.getModel();
        modelo.setRowCount(0); // Limpiar la tabla antes de agregar los resultados filtrados

        try (BufferedReader br = new BufferedReader(new FileReader("objetos.txt"))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(",");
                // Filtrar según el código
                if (codigo.isEmpty() || datos[0].toLowerCase().contains(codigo)) {
                    modelo.addRow(datos); // Agregar la fila filtrada a la tabla
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnIniciarCodigoActionPerformed

