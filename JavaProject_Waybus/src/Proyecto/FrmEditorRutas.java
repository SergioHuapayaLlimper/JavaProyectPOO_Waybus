    private void cargarCombos() {
        // Horarios
        List<String> horarios = MantenimientoRutas.obtenerHorarios();
        for (String h : horarios) {
            cmbHorario.addItem(h);
        }

        // Lugares
        List<String> lugares = MantenimientoRutas.obtenerLugares();
        for (String lugar : lugares) {
            cmbLugarPartida.addItem(lugar);
            cmbLugarLlegada.addItem(lugar);
        }
    }
    
    private void cargarConductores() {
        ArrayList<String> lista = MantenimientoRutas.obtenerConductores();
        for (String cod : lista) {
            cmbConductor.addItem(cod);
        }
    }
    
    private void cargarBuses() {
        ArrayList<String> lista = MantenimientoRutas.obtenerBusesDisponibles();
        for (String bus : lista) {
            cmbBus.addItem(bus);
        }
    }
    
    private void actualizarPrecio() {
        String salida = cmbLugarPartida.getSelectedItem() != null ? cmbLugarPartida.getSelectedItem().toString() : "";
        String llegada = cmbLugarLlegada.getSelectedItem() != null ? cmbLugarLlegada.getSelectedItem().toString() : "";

        String precio = MantenimientoRutas.obtenerPrecioDeRuta(salida, llegada);
        txtPrecio.setText(precio);
    }
    
    public static void abrirConDatos(String[] datos, int filaSeleccionada, FrmListaRutas frmAnterior) {
        FrmEditorRutas editor = new FrmEditorRutas();
        editor.txtFecha.setText(datos[0].trim());
        editor.txtPrecio.setText(datos[1].trim());
        editor.cmbHorario.setSelectedItem(datos[2].trim());
        editor.cmbLugarPartida.setSelectedItem(datos[3].trim());
        editor.cmbLugarLlegada.setSelectedItem(datos[4].trim());
        editor.cmbConductor.setSelectedItem(datos[5].trim());
        editor.cmbBus.setSelectedItem(datos[6].trim());

        editor.filaSeleccionada = filaSeleccionada;
        editor.frmPadre = frmAnterior; // guardamos la instancia anterior
        editor.setVisible(true);
    }


