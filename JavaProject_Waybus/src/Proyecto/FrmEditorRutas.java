    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String fecha = txtFecha.getText().trim();
        String precio = txtPrecio.getText().trim();
        String horario = cmbHorario.getSelectedItem().toString();
        String salida = cmbLugarPartida.getSelectedItem().toString();
        String llegada = cmbLugarLlegada.getSelectedItem().toString();
        String conductor = cmbConductor.getSelectedItem().toString();
        String bus = cmbBus.getSelectedItem().toString();

        // Validación
        if (fecha.isEmpty() || precio.equalsIgnoreCase("Ruta no permitida")) {
            JOptionPane.showMessageDialog(this, "Por favor completa los campos correctamente.");
            return;
        }

        String[] nuevaRuta = {fecha, precio, horario, salida, llegada, conductor, bus};

        MantenimientoRutas.actualizarRutaEnArchivo(filaSeleccionada, nuevaRuta);

        JOptionPane.showMessageDialog(this, "Ruta actualizada correctamente.");

        this.dispose(); // Cierra este formulario (FrmEditorRutas)

        if (frmPadre != null) {
            frmPadre.dispose(); // Cierra el formulario anterior (FrmListaRutas)
        }

        new FrmListaRutas().setVisible(true); // Abre uno nuevo con datos actualizados
    }//GEN-LAST:event_btnGuardarActionPerformed

