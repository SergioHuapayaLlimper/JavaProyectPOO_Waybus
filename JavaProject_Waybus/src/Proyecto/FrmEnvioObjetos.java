    private void mostrarDatosObjetoSeleccionado() {
        String codigoSeleccionado = (String) cbmCodigo.getSelectedItem();
        if (codigoSeleccionado == null || codigoSeleccionado.equals("Selecciona un código")) return;

        Logistica obj = MantenimientoLogistica.buscarObjetoPorCodigo(codigoSeleccionado);
        if (obj != null) {
            txtFecha.setText(obj.getFecha());
            txtObjeto.setText(obj.getObjeto());
            txtCantidad.setText(String.valueOf(obj.getCantidad()));
            txtOficina.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró el objeto para el código seleccionado.");
        }
    }
    
