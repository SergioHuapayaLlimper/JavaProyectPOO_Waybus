    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String codigo = (String) cbmCodigo.getSelectedItem();
        String fecha = txtFecha.getText().trim();
        String objeto = txtObjeto.getText().trim();
        String cantidadStr = txtCantidad.getText().trim();
        String area = txtOficina.getText().trim();

        if (codigo == null || codigo.equals("Selecciona un código") ||
            fecha.isEmpty() || objeto.isEmpty() || cantidadStr.isEmpty() || area.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
            return;
        }

        int cantidad;
        try {
            cantidad = Integer.parseInt(cantidadStr);
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor que cero.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Cantidad inválida.");
            return;
        }

        // Restar cantidad en objetos.txt
        boolean operacionExitosa = MantenimientoLogistica.descontarCantidadObjeto(codigo, cantidad);
        if (!operacionExitosa) {
            JOptionPane.showMessageDialog(this, "No hay suficiente stock para enviar esa cantidad.");
            return;
        }

        // Registrar el envío en envios.txt
        Logistica envio = new Logistica(codigo, fecha, objeto, cantidad, area);
        MantenimientoLogistica.registrarEnvio(envio);

        JOptionPane.showMessageDialog(this, "Envío registrado y stock actualizado correctamente.");
    }//GEN-LAST:event_btnEditarActionPerformed

