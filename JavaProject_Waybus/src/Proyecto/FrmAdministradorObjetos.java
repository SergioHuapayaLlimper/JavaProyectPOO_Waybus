    private void personalizarBoton(JButton boton) {
        Color fondo = new Color(0, 120, 215);         // Azul principal
        Color fondoHover = new Color(0, 150, 255);    // Hover más claro
        Color texto = Color.WHITE;

        boton.setBackground(fondo);
        boton.setForeground(texto);
        boton.setFocusPainted(false);
        boton.setBorder(BorderFactory.createEmptyBorder(10, 20, 10, 20));
        boton.setFont(new Font("Segoe UI", Font.BOLD, 14));
        boton.setCursor(new Cursor(Cursor.HAND_CURSOR));
        boton.setContentAreaFilled(true);
        boton.setOpaque(true);

        // Hover
        boton.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseEntered(java.awt.event.MouseEvent evt) {
                boton.setBackground(fondoHover);
            }

            @Override
            public void mouseExited(java.awt.event.MouseEvent evt) {
                boton.setBackground(fondo);
            }
        });
    }
   
    private void mostrarDatosObjetoSeleccionado() {
        String codigoSeleccionado = (String) cbmCodigo.getSelectedItem();
        if (codigoSeleccionado == null || codigoSeleccionado.equals("Selecciona un código")) return;

        try (BufferedReader br = new BufferedReader(new FileReader("objetos.txt"))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] partes = linea.split(",");
                if (partes.length >= 5 && partes[0].equals(codigoSeleccionado)) {
                    txtFecha.setText(partes[1]);     // 2025-06-18 01:10:38
                    txtObjeto1.setText(partes[2]);   // escobas
                    txtCantidad.setText(partes[3]);  // 34
                    txtArea.setText(partes[4]);      // lima
                    break;
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer los datos del objeto: " + e.getMessage());
        }
    }
    
