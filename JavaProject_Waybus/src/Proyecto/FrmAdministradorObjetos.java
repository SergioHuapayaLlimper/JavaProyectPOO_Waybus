    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String codigo = (String) cbmCodigo.getSelectedItem();
        String fecha = txtFecha.getText().trim();
        String objeto = txtObjeto1.getText().trim();
        String cantidadStr = txtCantidad.getText().trim();
        String area = txtArea.getText().trim();

        // Validación de selección de código
        if (codigo == null || codigo.equals("Selecciona un código")) {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona un código válido.");
            return;
        }

        // Validación de campos vacíos
        if (fecha.isEmpty() || objeto.isEmpty() || cantidadStr.isEmpty() || area.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar completos.");
            return;
        }

        // Validar que la cantidad sea un número entero
        int cantidad;
        try {
            cantidad = Integer.parseInt(cantidadStr);
            if (cantidad < 0) {
                JOptionPane.showMessageDialog(this, "La cantidad no puede ser negativa.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser un número entero.");
            return;
        }

        // Crear objeto actualizado
        Logistica objetoEditado = new Logistica(codigo, fecha, objeto, cantidad, area);

        // Llamar al método de actualización
        MantenimientoLogistica.editarObjeto(objetoEditado);

        // Confirmación
        JOptionPane.showMessageDialog(this, "Objeto actualizado correctamente.");
    }//GEN-LAST:event_btnEditarActionPerformed

